        res += sp4+"x__id = "+hex(self.id_val)+"\n"
        res += sp4+"x__ver = "+hex(GLB.VER_ID)+"\n"
        res += sp4+"x__fields = {\n"        
        for a_r in self.areas:
            if a_r.obj is None:
                # Registers area
                # Add two standard register - ID and VER
                adr = a_r.adr
                res += sp8+"'ID':("+hex(adr)+",(AwSreg,)),\\\n"
                res += sp8+"'VER':("+hex(adr+1)+",(AwSreg,)),\\\n"
